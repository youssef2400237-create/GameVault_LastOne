// ============= State =============
let currentLang  = localStorage.getItem("gv_lang")  || "en";
let currentTheme = localStorage.getItem("gv_theme") || "dark";

// ============= Apply on load =============
document.addEventListener("DOMContentLoaded", function () {
  applyTheme(currentTheme);
  applyLang(currentLang);
  injectControls();
});

// ============= Inject toggle buttons =============
function injectControls() {
  // For main pages: inject into .nav-icons
  const navIcons = document.querySelector(".nav-icons");
  if (navIcons) {
    const themeBtn = createThemeBtn();
    const langBtn  = createLangBtn();
    navIcons.insertBefore(themeBtn, navIcons.firstChild);
    navIcons.insertBefore(langBtn,  navIcons.firstChild);
  }

  // For dashboard: inject into .topbar-actions
  const topbarActions = document.querySelector(".topbar-actions");
  if (topbarActions) {
    const themeBtn = createThemeBtn();
    const langBtn  = createLangBtn();
    topbarActions.insertBefore(themeBtn, topbarActions.firstChild);
    topbarActions.insertBefore(langBtn,  topbarActions.firstChild);
  }

  // For login page: inject below form
  const loginPage = document.querySelector(".login_page");
  if (loginPage) {
    const controlBar = document.createElement("div");
    controlBar.className = "login-controls";
    controlBar.style.cssText = "display:flex;gap:0.75rem;justify-content:flex-end;margin-top:-0.5rem;";
    controlBar.appendChild(createThemeBtn());
    controlBar.appendChild(createLangBtn());
    loginPage.appendChild(controlBar);
  }
}

function createThemeBtn() {
  const btn = document.createElement("button");
  btn.className = "icon-button theme-toggle-btn";
  btn.title = "Toggle theme";
  btn.innerHTML = currentTheme === "dark"
    ? '<i class="fas fa-sun"></i>'
    : '<i class="fas fa-moon"></i>';
  btn.addEventListener("click", toggleTheme);
  return btn;
}

function createLangBtn() {
  const btn = document.createElement("button");
  btn.className = "icon-button lang-toggle-btn";
  btn.title = "Switch language / تغيير اللغة";
  btn.style.cssText = "font-weight:700;font-size:0.8rem;min-width:36px;";
  btn.textContent = currentLang === "en" ? "AR" : "EN";
  btn.addEventListener("click", toggleLang);
  return btn;
}

// ============= Theme =============
function toggleTheme() {
  currentTheme = currentTheme === "dark" ? "light" : "dark";
  localStorage.setItem("gv_theme", currentTheme);
  applyTheme(currentTheme);
  // update button icons
  document.querySelectorAll(".theme-toggle-btn").forEach(btn => {
    btn.innerHTML = currentTheme === "dark"
      ? '<i class="fas fa-sun"></i>'
      : '<i class="fas fa-moon"></i>';
  });
}

function applyTheme(theme) {
  document.documentElement.classList.toggle("light-mode", theme === "light");
}

// ============= Language =============
function toggleLang() {
  currentLang = currentLang === "en" ? "ar" : "en";
  localStorage.setItem("gv_lang", currentLang);
  applyLang(currentLang);
  document.querySelectorAll(".lang-toggle-btn").forEach(btn => {
    btn.textContent = currentLang === "en" ? "AR" : "EN";
  });
}

function applyLang(lang) {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  const t = translations[lang];

  // Replace text for all [data-i18n] elements
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (!t[key]) return;

    // Handle placeholder
    if (el.hasAttribute("placeholder")) {
      el.placeholder = t[key];
      return;
    }
    // Allow innerHTML for keys that contain HTML (like <br/>)
    if (t[key].includes("<")) {
      el.innerHTML = t[key];
    } else {
      el.textContent = t[key];
    }
  });
}
